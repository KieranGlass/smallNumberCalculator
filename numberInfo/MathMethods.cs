using System;

namespace numberInfo
{
    public class MathMethods
    { 

        public static int numPlus(int x, int y)
            {
                return x + y;
            }
        public static int numMinus(int x, int y)
            {
                return x - y;
            }
        public static int numMulti(int x, int y)
            {
                return x * y;
            }
        public static double numDiv(double x, double y)
            {
                return x / y;
            }
        public static double numSqu(double x, double y)
            {
            return Math.Sqrt(x + y);
            }
        
        
      
    }

}
